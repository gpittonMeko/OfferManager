def importa_lead():
    """Importa i lead ROV sul server"""
    with app.app_context():
        print("🔧 Inizializzazione database...")
        try:
            db.create_all()
            print("✅ Database inizializzato")
        except Exception as e:
            print(f"⚠️  Errore inizializzazione database: {e}")
        
        admin = User.query.filter_by(username='admin').first()
        if not admin:
            print("⚠️  Utente 'admin' non trovato. Creazione...")
            from werkzeug.security import generate_password_hash
            admin = User(
                username='admin',
                password=generate_password_hash('admin123'),
                role='admin'
            )
            db.session.add(admin)
            db.session.commit()
            print("✅ Utente admin creato")
        
        contatti_aggiunti = 0
        contatti_esistenti = 0
        
        print(f"\n📋 Importazione lead ROV...")
        print(f"Totale contatti da importare: {len(LEADS_DATA)}\n")
        
        for lead_data in LEADS_DATA:
            # Verifica se esiste già
            esistente = Lead.query.filter_by(
                email=lead_data.get('email', '')
            ).first()
            
            if esistente:
                contatti_esistenti += 1
                continue
            
            # Crea nuovo lead
            lead = Lead(
                company_name=lead_data.get('company_name', '')[:200],
                contact_first_name=lead_data.get('contact_first_name', '')[:100],
                contact_last_name=lead_data.get('contact_last_name', '')[:100],
                email=lead_data.get('email', '')[:100],
                phone=lead_data.get('phone', '')[:50] if lead_data.get('phone') else None,
                address=lead_data.get('address', '')[:500] if lead_data.get('address') else None,
                industry=lead_data.get('industry', 'Marina/Servizi Marittimi')[:100],
                interest=lead_data.get('interest', 'ROV'),
                source=lead_data.get('source', 'Sincronizzazione')[:50],
                rating=lead_data.get('rating', 'Warm'),
                status=lead_data.get('status', 'New'),
                notes=lead_data.get('notes', '')[:500],
                owner_id=admin.id,
                last_contact_at=None
            )
            
            db.session.add(lead)
            contatti_aggiunti += 1
        
        db.session.commit()
        
        total_rov = Lead.query.filter(
            (Lead.interest == 'ROV') | 
            (Lead.notes.contains('ROV')) |
            (Lead.industry.contains('Marina')) |
            (Lead.industry.contains('Biologia')) |
            (Lead.industry.contains('Subacquei')) |
            (Lead.industry.contains('Cantiere'))
        ).count()
        
        print(f"\n{'='*70}")
        print(f"✅ Completato!")
        print(f"   Contatti aggiunti: {contatti_aggiunti}")
        print(f"   Contatti già esistenti: {contatti_esistenti}")
        print(f"   Totale lead ROV nel database: {total_rov}")
        print(f"{'='*70}\n")

if __name__ == "__main__":
    importa_lead()
